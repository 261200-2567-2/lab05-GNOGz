public class Stat {
    private int atk;
    private int vit;
    private int agi;
    private int Int;

    public Stat(int atk, int vit, int agi, int Int) {
        this.atk = atk;
        this.vit = vit;
        this.agi = agi;
        this.Int = Int;
    }
    public int getAtk() {
        return atk;
    }
    public int getVit() {
        return vit;
    }
    public int getAgi() {
        return agi;
    }
    public int getInt() {
        return Int;
    }
}
